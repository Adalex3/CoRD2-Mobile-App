class PointData {
  final double latitude;
  final double longitude;
  final String description;
  final String title;
  final String eventType;
  final String formattedDate;
  final String creator;
  final String creatorId;

  PointData(
      this.latitude,
      this.longitude,
      this.description,
      this.title,
      this.eventType,
      this.formattedDate,
      this.creator,
      this.creatorId
      );

}