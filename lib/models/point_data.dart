class PointData {
  final double latitude;
  final double longitude;
  final String description;
  final String title;
  final String eventType;
  final String imageURL;
  final String formattedDate;
  final String creator;

  PointData(
      this.latitude,
      this.longitude,
      this.description,
      this.title,
      this.eventType,
      this.imageURL,
      this.formattedDate,
      this.creator
      );

}